namespace Framework
{
    /// <summary>
    /// 轻提示视觉类型，用于表现层选择颜色、图标和默认停留时长。
    /// </summary>
    public enum TipStyle
    {
        /// <summary>普通信息提示。</summary>
        Normal = 0,

        /// <summary>成功反馈提示。</summary>
        Success = 1,

        /// <summary>警告提示。</summary>
        Warning = 2,

        /// <summary>错误提示。</summary>
        Error = 3,
    }
}
